package entidades;

public class Arbol {
	//Atributos
			private int ID;
			private String NombreComun;
			private String NombreCientifico;
			private String Descripcion;	
			private String Multimedia;
			private String NombreDis;
			private String NombreFam;
			private String NombreFlo;
			private String NombreGenero;
			private int Estado;
			
			public int getID() {
				return ID;
			}
			public void setID(int iD) {
				ID = iD;
			}
			public String getNombreComun() {
				return NombreComun;
			}
			public void setNombreComun(String nombreComun) {
				NombreComun = nombreComun;
			}
			public String getNombreCientifico() {
				return NombreCientifico;
			}
			public void setNombreCientifico(String nombreCientifico) {
				NombreCientifico = nombreCientifico;
			}
			public String getDescripcion() {
				return Descripcion;
			}
			public void setDescripcion(String descripcion) {
				Descripcion = descripcion;
			}
			public String getMultimedia() {
				return Multimedia;
			}
			public void setMultimedia(String multimedia) {
				Multimedia = multimedia;
			}
			public String getNombreDis() {
				return NombreDis;
			}
			public void setNombreDis(String nombreDis) {
				NombreDis = nombreDis;
			}
			public String getNombreFam() {
				return NombreFam;
			}
			public void setNombreFam(String nombreFam) {
				NombreFam = nombreFam;
			}
			public String getNombreFlo() {
				return NombreFlo;
			}
			public void setNombreFlo(String nombreFlo) {
				NombreFlo = nombreFlo;
			}
			public String getNombreGenero() {
				return NombreGenero;
			}
			public void setNombreGenero(String nombreGenero) {
				NombreGenero = nombreGenero;
			}
			public int getEstado() {
				return Estado;
			}
			public void setEstado(int estado) {
				Estado = estado;
			}

}
